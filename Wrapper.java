public class Wrapper {
    public static void main(String[]args)
    {
        int p=20;
        System.out.println(p+20);
        System.out.println(Integer.toString(p)+20);
        String st="120";
        System.out.println(st+10);
        int s=Integer.parseInt(st);
        System.out.println(s+10);
    }
}
